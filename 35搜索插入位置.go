func searchInsert(nums []int, target int) int {
	size := len(nums)
	l, r := 0, size-1
	mid := 0
	for l <= r {
		mid = (r-l)/2 + l
		if target > nums[mid] {
			l = mid + 1
		} else if target < nums[mid] {
			r = mid - 1
		} else {
			return mid
		}
	}
	return r + 1
}

