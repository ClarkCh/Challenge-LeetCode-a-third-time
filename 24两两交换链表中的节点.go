func helper(pre *ListNode) *ListNode {
	tmp := pre.Next
	if tmp == nil || tmp.Next == nil {
		return tmp
	}
	//pre.Next, pre.Next.Next = pre.Next.Next, pre.Next

	pre.Next = tmp.Next
	tmp.Next = helper(pre.Next)
	pre.Next.Next = tmp
	return pre.Next
}
