func removeDuplicates(nums []int) int {
	size := len(nums)
	if size < 2 {
		return size
	}
	l := 0
	r := 1
	for r < size {
		if nums[l] < nums[r] {
			l++
			nums[l] = nums[r]
		}
		r++
	}
	return l+1
}
