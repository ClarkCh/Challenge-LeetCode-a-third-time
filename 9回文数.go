func isPalindrome(x int) bool {
	if x < 0 {
		return false
	}
	x2 := x
	rec := make([]int, 0)
	for x2 != 0 {
		rec = append(rec, x2% 10)
		x2 /= 10
	}
	tmp := 0
	for _, v := range rec {
		tmp = 10 * tmp + v
	}
	if x == tmp {
		return true
	}
	return false
}
