func maxSubArray(nums []int) int {
	size := len(nums)
	dp := make([]int, size)
	dp[0] = nums[0]
	for i := 1; i < size; i++ {
		tmp := dp[i-1] + nums[i]
		if tmp > nums[i] {
			dp[i] = tmp
		} else {
			dp[i] = nums[i]
		}
	}
	res := dp[0] - 1
	for _, v := range dp {
		if res < v {
			res = v
		}
	}
	return res
}

